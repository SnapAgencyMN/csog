  <h2>Welcome to the Community Septic System <br/> Owners Guide On-Line Tool</h2>
  <h3>About This Tool</h3>
  <p>This tool is designed to develop an Owner's Guide for everything from a single-family home, to a commercial property, to a cluster system serving 50+ homes. Some key things to keep in mind about the tool:</p>
  <ol>
    <li>It contains generic stock/boiler plate general information, system componenet descriptions and images, and operations and maintenance (O&M) recommendations</li>
    <li>It allows you to upload site specific descriptions, images and O&M recommendations</li>
    <li>It will produce a PDF guide For homeowners and businessess to use electronically or printed out</li>
    <li>It allows you to start and stop a project during the process and update it years later when things change</li>
    <li>It does NOT cover every scenario that exists across the US, but we hope it covers a vast majority of them!</li>
  </ol>
